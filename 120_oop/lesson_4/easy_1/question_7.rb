# What is the default thing that Ruby will print to the screen if
# you call to_s on an object? Where could you go to find out if you
# want to be sure?

# The default (if not overridden) is a string representing
# the object's class and an encoding of the object id.
# The top-level object returns 'main'.
# See the Ruby documentation for Object#to_s